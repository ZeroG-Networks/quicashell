// Crypto/TLS integration for QUIC, mostly from RFC 9001.

const std = @import("std");

const expectEqualSlices = std.testing.expectEqualSlices;

const sha256 = std.crypto.hash.sha2.Sha256;

// Configuration per Section 5.2 of RFC 9001.
const initialHkdf = std.crypto.kdf.hkdf.HkdfSha256;
const initialSalt = [_]u8{ 0x38, 0x76, 0x2c, 0xf7, 0xf5, 0x59, 0x34, 0xb3, 0x4d, 0x17, 0x9a, 0xe6, 0xa4, 0xc8, 0x0c, 0xad, 0xcc, 0xbb, 0x7f, 0x0a };

const hkdfExpandLabel = std.crypto.tls.hkdfExpandLabel;

// Test data below comes from Appendix A of RFC 9001.
const testConnectionId = [_]u8{ 0x83, 0x94, 0xc8, 0xf0, 0x3e, 0x51, 0x57, 0x08 };
const testInitialSecret = [_]u8{ 0x7d, 0xb5, 0xdf, 0x06, 0xe7, 0xa6, 0x9e, 0x43, 0x24, 0x96, 0xad, 0xed, 0xb0, 0x08, 0x51, 0x92, 0x35, 0x95, 0x22, 0x15, 0x96, 0xae, 0x2a, 0xe9, 0xfb, 0x81, 0x15, 0xc1, 0xe9, 0xed, 0x0a, 0x44 };
const testClientInitialSecret = [_]u8{ 0xc0, 0x0c, 0xf1, 0x51, 0xca, 0x5b, 0xe0, 0x75, 0xed, 0x0e, 0xbf, 0xb5, 0xc8, 0x03, 0x23, 0xc4, 0x2d, 0x6b, 0x7d, 0xb6, 0x78, 0x81, 0x28, 0x9a, 0xf4, 0x00, 0x8f, 0x1f, 0x6c, 0x35, 0x7a, 0xea };
const testClientKey = [_]u8{ 0x1f, 0x36, 0x96, 0x13, 0xdd, 0x76, 0xd5, 0x46, 0x77, 0x30, 0xef, 0xcb, 0xe3, 0xb1, 0xa2, 0x2d };
const testClientIv = [_]u8{ 0xfa, 0x04, 0x4b, 0x2f, 0x42, 0xa3, 0xfd, 0x3b, 0x46, 0xfb, 0x25, 0x5c };
const testClientHp = [_]u8{ 0x9f, 0x50, 0x44, 0x9e, 0x04, 0xa0, 0xe8, 0x10, 0x28, 0x3a, 0x1e, 0x99, 0x33, 0xad, 0xed, 0xd2 };

test "generate client in" {
    const initialSecret = initialHkdf.extract(&initialSalt, &testConnectionId);
    try expectEqualSlices(u8, &initialSecret, &testInitialSecret);

    const clientInitialSecret = hkdfExpandLabel(initialHkdf, initialSecret, "client in", "", 32);
    try expectEqualSlices(u8, &clientInitialSecret, &testClientInitialSecret);

    const clientKey = hkdfExpandLabel(initialHkdf, clientInitialSecret, "quic key", "", 16);
    try expectEqualSlices(u8, &clientKey, &testClientKey);
    const clientIv = hkdfExpandLabel(initialHkdf, clientInitialSecret, "quic iv", "", 12);
    try expectEqualSlices(u8, &clientIv, &testClientIv);
    const clientHp = hkdfExpandLabel(initialHkdf, clientInitialSecret, "quic hp", "", 16);
    try expectEqualSlices(u8, &clientHp, &testClientHp);
}
